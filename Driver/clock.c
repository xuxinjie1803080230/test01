/*~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
* 函数名  ： Clock32MInit
* 参数    ： void
* 返回    ： void
* 作者    ： xuxinjie
* 时间    ： 2020/11/24
* 描述    ： 32M时钟晶振初始化
~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~*/
void Clock32MInit(void)
{
    CLKCONCMD &= ~0x40;//设置系统时钟源为32MHZ晶振
    while(CLKCONSTA & 0x40);//等待晶振稳定
    CLKCONCMD &= ~0x47;//设置系统主时钟频率为32MHZ
}
/*~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
* 函数名  ： Clock16MInit
* 参数    ： void
* 返回    ： void
* 作者    ： xuxinjie
* 时间    ： 2020/11/24
* 描述    ： 16M时钟晶振初始化
~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~*/
void Clock16MInit(void)
{
     CLKCONCMD |= 0x40;//设置系统时钟源为16MHZ晶振
    while(CLKCONSTA & 0x40);//等待晶振稳定
    CLKCONCMD |= ~0x41;//设置系统主时钟频率为16MHZ

}
